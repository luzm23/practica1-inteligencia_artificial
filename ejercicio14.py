nota = int(input("ingrese tu calificacion (0-100): "))

if 90 <= nota <= 100:
    print("tu calificacion es : 'A'")
if 80 <= nota <= 90:
     print("tu calificacion es : 'B'")
if 70 <= nota <= 80:
    print("tu calificacion es : 'C'")
if 60 <= nota <= 70:
     print("tu calificacion es : 'D'")
else:
     print("tu calificacion es : 'F'")